    "      } else {\n",
    "        print('Less than 5')\n",
    "      }\n",
    "      ```\n\n",
    "  - **`ifelse(test, yes, no)`**: Vectorized if-else for quick checks.\n",
    "    - Example: `ifelse(1:5 %% 2 == 0, 'even', 'odd')`\n\n",
    "  - Combine conditions:\n",
    "    - **`&&`**: Logical AND (both conditions must be TRUE).\n",
    "    - **`||`**: Logical OR (at least one condition must be TRUE).\n",
    "    - **`!`**: Logical NOT (negates TRUE to FALSE).\n",
    "    - Example: `if(x > 0 && y < 0)`\n\n",
    "---\n\n",
    "### 2. Loops\n",
    "  - **`for(i in 1:10)`**: Iterates over a range or vector.\n",
    "    - Example:\n",
    "      ```R\n",
    "      for(i in 1:5) {\n",
    "        print(i)\n",
    "      }\n",
    "      ```\n\n",
    "  - **`while(cond)`**: Repeats while `cond` is TRUE.\n",
    "    - Example:\n",
    "      ```R\n",
    "      wallet <- 10\n",
    "      while(wallet > 0) {\n",
    "        wallet <- wallet - 2\n",
