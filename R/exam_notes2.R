    "  - **`if(cond)`**: Runs code if `cond` is TRUE. Use `else if` and `else` for alternatives.\n",
    "    - Example:\n",
    "      ```R\n",
    "      x <- 5\n",
    "      if(x > 10) {\n",
    "        print('Greater than 10')\n",
